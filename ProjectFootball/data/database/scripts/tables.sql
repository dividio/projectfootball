BEGIN TRANSACTION;

CREATE TABLE PF_GAME_STATES
(
  S_STATE TEXT
  ,X_STATE INTEGER PRIMARY KEY AUTOINCREMENT
  ,S_VALUE TEXT
);

CREATE TABLE PF_MATCHES
(
  D_MATCH TEXT
  ,L_PLAYED TEXT
  ,X_FK_TEAM_HOME INTEGER
  ,X_MATCH INTEGER PRIMARY KEY AUTOINCREMENT
  ,X_FK_TEAM_AWAY INTEGER
);

CREATE TABLE PF_GAME_OPTIONS
(
  S_CATEGORY TEXT
  ,X_OPTION INTEGER PRIMARY KEY AUTOINCREMENT
  ,S_VALUE TEXT
  ,S_ATTRIBUTE TEXT
);

CREATE TABLE PF_TEAM_PLAYER_CONTRACTS
(
  D_END TEXT
  ,D_BEGIN TEXT
  ,X_FK_TEAM INTEGER
  ,X_FK_TEPL INTEGER
  ,X_TPCO INTEGER PRIMARY KEY AUTOINCREMENT
);

CREATE TABLE PF_GOALS
(
  X_FK_TEPL_SCORER INTEGER
  ,X_GOAL INTEGER PRIMARY KEY AUTOINCREMENT
  ,L_OWN_GOAL TEXT
  ,X_FK_TEAM_SCORER INTEGER
  ,X_FK_MATCH INTEGER
  ,N_MINUTE INTEGER
);

CREATE TABLE PF_TEAM_PLAYERS
(
  X_TEPL INTEGER PRIMARY KEY AUTOINCREMENT
  ,S_NAME TEXT
);

CREATE TABLE PF_TEAMS
(
  X_TEAM INTEGER PRIMARY KEY AUTOINCREMENT
  ,S_TEAM TEXT
);

COMMIT;
