  def interfaceName(self, cadena) :
    cad = cadena
    cad = cad.lower()
    cad = cad.replace("_", " ")
    cad = cad.title()
    cad = cad.replace(" ", "")
    cad = "I" + cad
    return cad

  def className(self, cadena) :
    cad = cadena
    cad = cad.lower()
    cad = cad.replace("_", " ")
    cad = cad.title()
    cad = cad.replace(" ", "")
    cad = "C" + cad
    return cad

  def attributeName(self, cadena) :
    cad = cadena
    cad = cad.lower()
    cad = cad.replace("_", " ")
    cad = cad.title()
    cad = cad.replace(" ", "")
    return cad

  def defineH(self, cadena) :
    cad = cadena
    cad = cad.upper()
    cad = "#ifndef "+cad+"_H_\n"+\
          "#define "+cad+"_H_\n\n"
    return cad

  def endDefineH(self, cadena) :
    cad = cadena
    cad = cad.upper()
    cad = "#endif /*"+cad+"_H_*/\n"
    return cad

  def primaryKey(self, klass) :
    pk = ""
    for a in klass.attributes.keys() :
      if a[:2]=="X_" and a[:4]!="X_FK" :
        pk = a
    return pk

  def thereIsDateAttrib(self, klass) :
    for a in klass.attributes.keys() :
      t, v = klass.attributes[a]
      if t=="DATE" :
        return 1
    return 0


